package com.hotel.asia.dto;

public class Room {
	private int ROOM_ID;
	private String ROOM_TYPE;
	private int ROOM_PRICE;
	private String ROOM_IMG;
	private int ROOM_MAX;
	private String ROOM_DETAIL;
	private String ROOM_STATE;
	
	// getter, setter
	public int getROOM_ID() {
		return ROOM_ID;
	}
	public void setROOM_ID(int rOOM_ID) {
		ROOM_ID = rOOM_ID;
	}
	public String getROOM_TYPE() {
		return ROOM_TYPE;
	}
	public void setROOM_TYPE(String rOOM_TYPE) {
		ROOM_TYPE = rOOM_TYPE;
	}
	public int getROOM_PRICE() {
		return ROOM_PRICE;
	}
	public void setROOM_PRICE(int rOOM_PRICE) {
		ROOM_PRICE = rOOM_PRICE;
	}
	public String getROOM_IMG() {
		return ROOM_IMG;
	}
	public void setROOM_IMG(String rOOM_IMG) {
		ROOM_IMG = rOOM_IMG;
	}
	public int getROOM_MAX() {
		return ROOM_MAX;
	}
	public void setROOM_MAX(int rOOM_MAX) {
		ROOM_MAX = rOOM_MAX;
	}
	public String getROOM_DETAIL() {
		return ROOM_DETAIL;
	}
	public void setROOM_DETAIL(String rOOM_DETAIL) {
		ROOM_DETAIL = rOOM_DETAIL;
	}
	public String getROOM_STATE() {
		return ROOM_STATE;
	}
	public void setROOM_STATE(String rOOM_STATE) {
		ROOM_STATE = rOOM_STATE;
	}
	
	
}
